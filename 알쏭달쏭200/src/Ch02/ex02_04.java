package Ch02;
//3개의 int형 변수에 각각 값을 대입해서 합계와 평균을 구하는 프로그램을 작성해보세요!
public class ex02_04 {

	public static void main(String[] args) {
		int x, y, z;
		
		x=81;
		y=78;
		z=52;
		
		System.out.println("x의 값은"+x+"입니다");
		System.out.println("y의 값은"+y+"입니다");
		System.out.println("z의 값은"+z+"입니다");
		System.out.println("합계는"+(x+y+z)+"입니다");
		System.out.println("평균은"+(x+y+z)/3+"입니다");
	

	}

}
